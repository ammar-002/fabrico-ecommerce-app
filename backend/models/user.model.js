import mongoose from "mongoose";
import { Schema } from "mongoose";
const userSchema = new mongoose.Schema(
  {
    fullName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    phoneNumber: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      enum: ["admin", "user"],
      required: true,
      default: "user",
    },
    profilePic: {
      type: String,
      default: "",
    },
  },
  { timestamps: true }
);


export const User = mongoose.model("User", userSchema);
